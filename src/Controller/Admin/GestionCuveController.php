    /**
     * @Security("is_granted('ROLE_CLIENT') and user.getIsEnabled()")
     * @Route("/gestion/cuves", name="gestion_cuves.index", methods={"GET", "POST"})
     * @throws Exception
     */
    public function index(CuveRepository $cuveRepository, VenteCuveRepository $venteCuveRepository, Request $request, JaugeageRepository $jaugeageRepository, StockageRepository $stockageRepository, VentePistoletRepository $ventePistoletRepository): Response
    {
        $errors = [];
        $cuves = $cuveRepository->getCuveByStation($this->getUser()->getStructureClient()->getStations()[0]->getId());
        $typeCarburants = $this->getUser()->getStructureClient()->getStations()[0]->getTypeCarburants();
        $errorJaugeages = [];

        foreach ($cuves as $cuve) {
            if ($cuve->getStock() <= $cuve->getQteAlerte() && $cuve->getQteAlerte() !== 0 && $cuve->getIsActived()) {
                $errors[] = 'ALERTE DE STOCK POUR ' . $cuve->getNumero();
            }
            $dayJaugeage = $jaugeageRepository->getCuveDayJaugeage($cuve->getId());
            if ($dayJaugeage == []) {
                $errorJaugeages[$cuve->getNumero()] = 'LA ' . $cuve->getNumero() . ' N\'A PAS ETE JAUGEE AUJOURD\'HUI';
            }
        }

        $qteDepartEssence = 2392;
        $qteDepartGasoil = 379140;

        $dateDepart = date('2021-07-02 00:00:00');
        $typeEssenceId = 1;
        $typeGasoilId = 2;
        $qteStockageGasoil = doubleval($stockageRepository->getQteEntreeByTypeCarburant($typeGasoilId, $dateDepart)["SOMME"]);
        $qteStockageEssence = doubleval($stockageRepository->getQteEntreeByTypeCarburant($typeEssenceId, $dateDepart)["SOMME"]);
        // $qteSortieTotalCuveGasoil = round($venteCuveRepository->getVenteCuveByTypeCarburantAfterDate($typeGasoilId, $dateDepart)["QTE"]);
        // $qteSortieTotalCuveEssence = round($venteCuveRepository->getVenteCuveByTypeCarburantAfterDate($typeEssenceId, $dateDepart)["QTE"]);
        $qteSortieTotalPistoletGasoil = round($ventePistoletRepository->getVentePistoletByTypeCarburantAfterDate($typeGasoilId, $dateDepart)['QTE']);
        $qteSortieTotalPistoletEssence = round($ventePistoletRepository->getVentePistoletByTypeCarburantAfterDate($typeEssenceId, $dateDepart)['QTE']);
        // dd($qteSortieGasoil);
        $qteFictiveGasoil = $qteStockageGasoil + $qteDepartGasoil - $qteSortieTotalPistoletGasoil;
        $qteFictiveEssence = $qteStockageEssence + $qteDepartEssence - $qteSortieTotalPistoletEssence;

        // dd($qteFictifEssence);

        $lasVenteTypeCaburant = [];
        $arrayQteStock = [];
        $dateInf = $dateSup = null;
        if ($_SERVER['REQUEST_METHOD'] == 'POST') {
            $date1 = $request->get('dateInf');
            $date2 = $request->get('dateSup');
            if ($date1 >= $date2) {
                $this->addFlash('danger', 'Erreur de date pour les sortis cuve');
                return $this->redirectToRoute('gestion_cuves.index');
            } else {
                $dateInf = new DateTime($date1, new DateTimeZone('GMT'));
                $dateSup = new DateTime($date2, new DateTimeZone('GMT'));
            }
        }

        if ($_SERVER['REQUEST_METHOD'] == 'POST' && $dateInf != null && $dateSup != null) {
            $this->addFlash('success', 'Les sorties entre ' . $dateInf->format('d-m-Y') . ' et ' . $dateSup->format('d-m-Y'));
        } else {
            $this->addFlash('success', 'Les sorties du jour');
        }

        foreach ($typeCarburants as $typeCarburant) {
            $qte = 0;
            $montantAchat = 0;
            $montantVente = 0;
            $gain = 0;
            $founded = false;
            $type = $typeCarburant->getName();
            $qteStock = 0;
            $dateV = null;

            foreach ($typeCarburant->getCuves() as $cuve) {
                $qteStock = $qteStock + $cuve->getStock();
                if ($_SERVER['REQUEST_METHOD'] == 'POST' && $dateInf != null && $dateSup != null) {
                    $ventes = $venteCuveRepository->getCuveVentesByDate($dateInf, $dateSup, $cuve->getId());
                } else {
                    $ventes = $venteCuveRepository->getCuveYesterDayVente($cuve->getId());
                }

                foreach ($ventes as $vente) {
                    if ($vente != null) {
                        $founded = true;
                        $qte = $qte + $vente->getQuantite();
                        $montantAchat = $montantAchat + $vente->getMontantAchat();
                        $montantVente = $montantVente + $vente->getMontantVente();
                        $gain = $gain + $vente->getGain();
                        $dateV = $vente->getCreatedAt();
                    }
                }
            }

            $arrayQteStock[$type] = $qteStock;
            if ($founded) {
                $venteT = [];
                $venteT['type'] = $typeCarburant->getName();
                $venteT['qte'] = $qte;
                $venteT['montantAchat'] = $montantAchat;
                $venteT['montantVente'] = $montantVente;
                $venteT['gain'] = $gain;
                $venteT['date'] = $dateV;
                $lasVenteTypeCaburant[] = $venteT;
            }
        }

        return $this->render('user-client/gestion/cuve/index.html.twig', [
            'cuves' => $cuves,
            'errors' => $errors,
            'cuveYesterDayVentes' => $lasVenteTypeCaburant,
            'arrayQteStock' => $arrayQteStock,
            'errorJaugeages' => $errorJaugeages,
            'qteEssenceFromStockage' => $qteFictiveEssence,
            'qteGasoilFromStockage' => $qteFictiveGasoil
        ]);
    }

    /**
     * @Security("is_granted('ROLE_CLIENT') and user.getIsEnabled()")
     * @Route("/gestion/cuves/{id}/show", name="gestion_cuves.show")
     * @param Cuve $cuve
     * @param VenteCuveRepository $venteCuveRepository
     * @param PaginatorInterface $paginator
     * @param Request $request
     * @return Response
     */
    public function showCuve(Cuve $cuve, VenteCuveRepository $venteCuveRepository, PaginatorInterface $paginator, Request $request): Response
    {
        $ventes = $venteCuveRepository->findBy(['cuve' => $cuve], ['createdAt' => 'DESC']);

        return $this->render('user-client/gestion/cuve/show_cuve.html.twig', [
            'cuve' => $cuve,
            'percentage' => ($cuve->getStock() * 100) / $cuve->getCapacity(),
            'ventes' => $paginator->paginate($ventes, $request->query->getInt('page', 1), 30)
        ]);
    }

    /**
     * @Security("is_granted('ROLE_CLIENT') and user.getIsEnabled()")
     * @Route("/gestion/cuves/changement-etat-cuves", name="gestion_cuves.activation", methods={"GET", "POST"})
     * @param Request $request
     * @return Response
     */
    public function activationCuve(Request $request): Response
    {
        $cuves = $this->getUser()->getStructureClient()->getStations()[0]->getCuves();
        if ($_SERVER['REQUEST_METHOD'] == 'POST') {
            foreach ($cuves as $cuve) {
                if ($request->get($cuve->getNumero())) {
                    $cuve->setIsActived(true);
                } else {
                    $cuve->setIsActived(false);
                }

                $this->getDoctrine()->getManager()->flush();
            }

            $this->addFlash('success', 'Les cuves ont été mis à jour');
            return $this->redirectToRoute('gestion_cuves.index');
        }

        return $this->render('user-client/gestion/cuve/index.html.twig', [
            'cuves' => $cuves
        ]);
    }

