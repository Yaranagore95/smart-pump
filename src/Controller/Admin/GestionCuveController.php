    /**
     * @param $cuveId
     * @param array $content
     * @param CuveRepository $cuveRepository
     */
    public function saveCuveMesure($cuveId, array $content, CuveRepository $cuveRepository)
    {
        $mesureArray = [];

        $cpt = 1;
        for ($i = 0; $i < sizeof($content); $i++) {
            if ($cpt % 2 == 0) {
                $mesureArray[] = $content[$i];
            }
            $cpt++;
        }

        $cpt = 1;
        for ($i = 0; $i < sizeof($mesureArray); $i++) {
            $mesure = new CuveMesure();
            $mesure->setCuve($cuveRepository->findOneBy(['id' => $cuveId]));
            $mesure->setLevelCM($i + 1);
            $mesure->setVolume($mesureArray[$i]);
            $m = $this->getDoctrine()->getManager();
            $m->persist($mesure);
            $m->flush();
            $cpt++;
        }
    }
